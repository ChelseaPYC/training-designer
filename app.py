def _feature_row(name, tag, desc, image_key, reverse=False):
    """渲染一行功能模块：左侧内容 + 右侧图片（reverse 时互换）

    FEATURE_IMAGES[image_key] 可能是：
    - PIL Image 对象（v5.8+ 硬编码 base64 解码）
    - 字符串路径（兼容旧版）
    """
    content_html = f"""
    <div class="feature-content">
        <div class="feature-tag">{tag}</div>
        <div class="feature-name">{name}</div>
        <div class="feature-desc">{desc}</div>
    </div>
    """
    # reverse=True 时图片在左，文字在右；reverse=False 时图片在右，文字在左
    if reverse:
        c_img, c_text = st.columns([45, 55], gap="large")
    else:
        c_text, c_img = st.columns([55, 45], gap="large")

    with c_text:
        st.markdown(content_html, unsafe_allow_html=True)
    with c_img:
        img_obj = FEATURE_IMAGES.get(image_key)
        rendered = False
        if img_obj is not None:
            # 第一次尝试：直接传（兼容 PIL Image 对象、URL、路径字符串）
            try:
                st.image(img_obj, use_container_width=True)
                rendered = True
            except Exception:
                pass
            # 第二次尝试：如果是字符串路径，解析为绝对路径再试
            if not rendered and isinstance(img_obj, str):
                try:
                    abs_path = str(Path(__file__).resolve().parent / "images" / Path(img_obj).name)
                    st.image(abs_path, use_container_width=True)
                    rendered = True
                except Exception:
                    pass
        if not rendered:
            st.markdown(
                f'<div class="feature-image-fallback">{name}</div>',
                unsafe_allow_html=True,
            )

