@st.cache_data(show_spinner=False)
def _load_feature_image(b64: str) -> io.BytesIO:
    """base64 字符串 → BytesIO 字节流。@st.cache_data 保证只解码一次。

    为什么用 BytesIO 而不是 PIL Image：
    - st.image() 同时支持两者
    - BytesIO 是纯字节流，Streamlit 跨平台序列化更可靠
    - 不依赖 PIL，requirements.txt 可去掉 Pillow
    - 字节流开头三个字节 0xFF 0xD8 0xFF 是 JPEG 魔数
    """
    raw = base64.b64decode(b64)
    return io.BytesIO(raw)

